void LCD_RW(unsigned char wr)                //LCD  0-读 否则 写
{
     if(!wr) GPIOD->ODR &= ~(1 << 9);	
     else   GPIOD->ODR |=  (1 << 9);
} 
void LCD_CD(unsigned char dat)               //LCD  0-写命令 1-写数据
{
     if (dat) GPIOD->ODR |= (1 << 8);		
     else	GPIOD->ODR &= ~(1 << 8);			
} 
void LCD_EN(unsigned char en)                //LCD 操作使能
{
  if (en) GPIOD->ODR |= (1 << 10);
  else	  GPIOD->ODR &= ~(1 << 10);
} 
void LCD_WrByte(unsigned char dat)            // 写字节
{
  GPIOD->ODR = ((GPIOD->ODR & 0xFF00) | dat);	
}
unsigned char  LCD_RdByte(void)  // 读字节
{
  return (  (GPIOD->IDR & 0x00FF)  );	      
}
void LCD_RST(unsigned char en)               // 复位
{
   if (en==0)  GPIOD->ODR &= ~(1 << 11);
   else	    GPIOD->ODR |=  (1 << 11);
}

void LCD_ShipCS(unsigned cs)                  //片选 0,1,2;  3-不选  有3个芯片封装在内  每个芯片 64*64
{
   switch(cs){
         case 0:// 选择第1区
                GPIOD->ODR &= ~(1 << 13);// CS1 low
                GPIOD->ODR &= ~(1 << 12);// CS2 low
                break;
         case 1:// 选择第2区
                GPIOD->ODR |= (1 << 13);	
                GPIOD->ODR &= ~(1 <<12);// CS2 low
                break;
         case 2:// 选择第3区
                GPIOD->ODR &= ~(1 << 13);	// CS1 low
                GPIOD->ODR |=  (1 << 12); // CS2 high
                break;
         case 3:// 3-不选
                GPIOD->ODR |= (1 << 13);	
                GPIOD->ODR |= (1 << 12);
                break;
			}  
}
