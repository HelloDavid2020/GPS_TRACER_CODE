extern const uint8  FONT8x8ASCII[][8];
uint8  user_put_char8_8(uint32 x, uint32 y, uint8 ch)
{  uint8   font_dat;
   uint8   i, j;
   TCOLOR  bakc;

   /* 参数过滤 */
//   if( x>=(GUI_LCM_XMAX-8) ) return(x=0);
//   if( y>=(GUI_LCM_YMAX-8) ) return(0);
   if( (ch<0x20) || (ch>0x7f) ) ch = 0x20;
   
   ch -= 0x20; 
   for(i=0; i<8; i++)
   {  /* 读取点阵数据 */
      font_dat = FONT8x8ASCII[ch][i];
      
      for(j=0; j<8; j++)
      {  /* 设置相应的点为color或为back_color */
         if( (font_dat&DCB2HEX_TAB[j])==0 ) 
					 GUI_CopyColor(&bakc, back_color);
         else  
					 GUI_CopyColor(&bakc, disp_color);
				 
         GUI_Point(x, y, bakc);       
         x++;
      }
      
      y++;									// 指向下一行
      x -= 8;								// 恢复x值
   }
   
   return(1);
}




void  show_rx_buf(char *str)
{  
	uint32 x=0;
	uint32 y=32;	
	while(1)
   {  
		 if( (*str)=='\0' ) break;
		 if( (*str)=='\n' ) 
		 {
			x=0;
			y+=8;
		}

			user_put_char8_8(x, y, *str++);
      x += 8;								// 下一个字符显示位置，y不变(即不换行)
		 if(x>=(GUI_LCM_XMAX)) 
		 {
				x=0;
				y+=8;
		 }
		 
		 if(y>=(GUI_LCM_YMAX))
			y=32;
		 
   }
}


